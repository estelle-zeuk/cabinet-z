                <p>
                    Les adhérents aux présents statuts affirment leur attachement aux termes de la charte des
                    valeurs et de la charte éthique de la <strong>Fédération « 1 million de Marcheurs »</strong>.
                </p>
                <p>
                    Le Principe garantit l'équilibre entre le principe de représentation, reconstruction, de
                    regroupement, de fédérer et de libre expression des adhérents et le principe de cohérence et de
                    responsabilité des instances élues.
                </p>
                <p>
                    Les Membres réunis en Assemblée Générale le 15 mai 2021 conviennent de créer une
                    Fédération « 1 Million de Marcheurs », une Fédération régie par la loi du 1er juillet 1901
                </p>
                <p>
                    <strong>1 Million de Marcheurs</strong> s’engage à travailler, à promouvoir les collectifs, les
                    associations, les mouvements politiques, les groupes de réflexion, autour des idées républicaines,
                    de l'écologie, économie, économie solidaire et le développement durable par l'édification
                    d'une démocratie de responsabilité dans la vie politique nationale, européenne et
                    internationale comme dans la vie économique sociale, écologique, libérale, institutionnelle,
                    environnementale … .
                </p>
                <p>
                    Les Adhérents de La Fédération « 1 Million de Marcheurs » s'engagent à respecter la
                    charte des valeurs et la charte éthique ainsi que le règlement intérieur annexés aux présents
                    statuts. Ils respectent ses choix de fonctionnement et ses décisions statutaires.
                    Chaque personne, groupe, association, club, institution, collectif…, a la place dans notre
                    Fédération
                </p>
                <p>
                    La Fédération 1 million de Marcheurs est la porte ouverte pour tous, La France et
                    l'Europe doivent être une chance pour tous.
                    La Fédération 1 Million de Marcheurs, Porte les actions de ces adhérents et les aident dans
                    leurs démarches dans leur développement, et les donne les moyens pour agir. La Fédération
                    fera de sorte que ces adhérents ne se sentent plus seule
                </p><br>

                <h5>Article 2 : Siège</h5>
                <p>
                    Le siège de la Fédération « 1 Million de Marcheurs est fixé au 28 Villa Sadi Carnot, 75019
                    Paris. Il pourra être transféré sur simple décision du bureau.
                </p>
                <h5>Article 3 : </h5>
                <p>
                    <strong>Durée :</strong> La durée de l’association est illimitée.
                </p>
                <h5>Article 4 : Objet</h5>
                <p>
                    La Fédération « 1Million de Marcheurs » son objectif est de regrouper tous les Associations,
                    les structures, les collectifs, les Mouvements politique, les Mouvements Européens, les
                    institutions, les groupes, les Clubs animés par la même volonté que nous, les militants,
                    militantes adhérents et non adhérents d’en marche, défendant les valeurs humaines,
                    démocratiques et progressistes.
                </p>
                <h5>Article 5 : Ressources</h5>
                <p>
                    Les Ressources de la Fédération « 1 Million de Marcheurs » sont constituées :
                </p>
                <ul>
                    <li>Des dons de ses membres;</li>
                    <br>
                    <li>Des dons et legs des personnes physiques ;</li>
                    <br>
                    <li>Des emprunts ;</li>
                    <li>De toute autre recette perçue dans le respect de la législation relative au
                        financement des Fédérations;</li>
                    <li>Des revenus tirés de ses services ou de son travail;</li>
                    <li>Des dons des personnes physiques autorisés par la loi</li>
                    <li>Des aides publiques prévues par la loi, Des contributions de personnes morales, le cas
                        échéant, des cotisations autorisées par la loi, versées par les membres adhérents, dans
                        les conditions le cas échéant précisées par le règlement intérieur.</li>
                    <li>
                        Des reversements d’indemnités d’élus;
                    </li>
                    <br>
                    <li>De tout autre produit autorisé par la loi;</li>
                    <li>Des cotisations et dons financiers ou matériels des adhérents ;</li>
                    <li>Des fruits tirés de placements financiers ;</li>
                    <li>Des versements d’organismes publics au titre de subvention ;</li>
                    <li>De la cotisation annuelle déterminée par le bureau.</li>
                </ul>
                <h5>Article 6 : Membres</h5>
                <p>
                    La Fédération « 1 Million de Marcheurs est composée de membres fondateurs, de membres
                    actifs, de membres associés, des volontaires, des groupes, des associations, des collectifs des
                    mouvements ou partis politiques, et de membres d’honneur. Tous ces membres composent les
                    adhérents de la Fédération.
                </p>
                <h6>A -  Membres fondateurs</h6>
                <p>
                    Les membres fondateurs sont les membres qui ont déposé les premiers statuts de la
                    Fédération « 1 million de Marcheurs », avec les membres de son bureau initial, ainsi que ceux
                    qui forment son premier Comité d'Éthique et de Déontologie
                </p>
                <h6>B -  Membres actifs</h6>
                <p>
                    Les conditions pour être membre actif sont les suivantes :
                    <ul>
                        <li>S’engager formellement par écrit, ou par e-mail, et se conformer aux dispositions des
                            statuts, au règlement intérieur et à la Charte d’Éthique et de Déontologie ;</li>
                        <li>S'acquitter une cotisation annuelle fixée par le bureau et votée par l’assemblée générale</li>
                    </ul>
                </p>
                <h6>C-  Membres bienfaiteurs</h6>
                <p>
                    Les membres bienfaiteurs sont des membres actifs qui ont réglé leur cotisation et qui ont fait
                    un don supplémentaire.
                </p>
                <h6>D-  Membres d'honneur</h6>
                <p>
                    Ils sont nommés par le bureau pour avoir rendu des services importants à la Fédération ou à
                    la République. Les membres d’honneur sont des membres actifs dispensés de cotisation, qui
                    n’ont donc pas de droit de vote. Ils peuvent recevoir le titre d’ambassadeur de Fédération.
                </p>
                <h5>Article 7 : Démission - Radiation</h5>
                <p>
                    La qualité de membre se perd automatiquement par la démission ou le décès, et par la
                    radiation prononcée par le bureau. Cette radiation peut avoir lieu en cas de non-paiement de
                    la cotisation deux années de suite, ou pour manquement à la Charte d’éthique et de
                    déontologie. L’intéressé est alors invité par lettre recommandée ou par e-mail avec accusé de
                    réception, à se présenter physiquement ou virtuellement par visio-conférence devant le
                    Bureau pour expliquer ce manquement. Faute de quoi, sa qualité de membre est suspendue
                    provisoirement sur décision du Comité d’éthique et de déontologie statuant à la majorité
                    simple ; il sera éventuellement révoqué par radiation après vote des membres du bureau à la
                    l’unanimité.
                </p>
                <h5>Article 8 : Charte d’éthique et de déontologie - Le Comité d'éthique et de déontologie</h5>
                <p>
                    La charte d’éthique et de déontologie est décrite dans le règlement intérieur. Ce règlement
                    doit recueillir l’approbation préalable du Comité d’éthique et de déontologie (CED) ou, à
                    défaut, de l’assemblée générale. Le CED est chargé de veiller au respect de la charte d’éthique
                    et de déontologie par la Fédération et par ses membres. Les missions du CED sont détaillées
                    dans le règlement intérieur.
                </p>
                <h5>Article 9 : Le bureau</h5>
                <p>
                    Le bureau, élu par l’assemblée générale, est composé d’un Président, d’un Vice-Président,
                    d’un Secrétaire général et d’un Trésorier. Le bureau a un rôle exécutif dans la réalisation des
                    missions de la Fédération « 1 Million de Marcheurs » :
                </p>
                <ul>
                    <li>Le Président est habilité à représenter la Fédération en justice, tant en demande qu’en
                        défense et ne peut effectuer aucun paiement en son nom. Il représente la Fédération
                        en région, auprès des pouvoirs publics, institutions publiques, entreprises privées et
                        de toutes institutions politiques (partis et mouvements), structures, collectifs,
                        Associations, Clubs... Il peut se faire assister de tout membre de la Fédération de son
                        choix;</li>
                    <li>Le Vice-Président seconde le Président. En cas d’empêchement du Président, il se
                        substitue à celui-ci. Il ne peut pas signer de contrat au nom de la Fédération, contracter
                        un prêt ou effectuer une transaction financière sans accord préalable et vote de
                        l’assemblée générale. Il représente la Fédération en région, auprès des pouvoirs
                        publics, institutions publiques, entreprises privées et de toutes institutions politiques
                        (partis et mouvements) …. Il peut se faire assister de tout membre de la Fédération de
                        son choix;</li>
                    <li>Le Secrétaire général rédige les procès-verbaux des réunions et de l’assemblée
                        générale, les transcrit, les signe et les adresse aux membres du bureau. Il fixe les
                        ordres du jour et convoque les membres du bureau et/ou les adhérents en concertation
                        avec le Président par courrier ou e-mail. Il est dépositaire des archives de la Fédération
                        et en assure la conservation conformément à la législation.</li>
                    <li>Le Trésorier est comptable des fonds de la Fédération. Il procède au recouvrement
                        des cotisations et au paiement des dépenses, sous validation et accord de l’expertcomptable de la Fédération. Il peut ouvrir et fermer un compte bancaire au nom de laFédération, à condition de prévenir et d’être d’accord avec le président ou la
                        présidente de la Fédération. Il est dépositaire des archives financières de l Fédération
                        et en assure la conservation conformément à la législation.</li>
                </ul>
                <p>Le bureau se réserve le droit de proposer l’élection de plusieurs vice-présidents avec des
                    thématiques, des secrétaires généraux adjoint(e)s et des trésoriers-adjoint(e)s. Sur proposition
                    du bureau, des délégués de région et de département pourront assister aux réunions de bureau
                    selon l’ordre du jour.</p>
                <h5>Article 10 : Assemblée générale</h5>
                <p>
                    L’assemblée générale réunit tous les adhérents, qu’ils soient membres fondateurs, membres
                    actifs et membres bienfaiteurs de la Fédération ainsi que les membres d’honneur, en séance
                    ordinaire au moins une fois par an. Si le Président de la Fédération est empêché, le bureau
                    peut désigner un(e) président(e) de séance.
                </p>
                <p>
                    L’assemblée générale délibère sur toutes les questions inscrites à l’ordre du jour à la majorité
                    des membres présents ou représentés. Aucun quorum n’est exigé dès lors que l’assemblée
                    générale a bien été convoquée selon les règles décrites ci-dessous :
                    <ul>
                    <li>L’assemblée générale est convoquée par le(la) Président(e), ou sur motion d’au moins
                        un quart des membres fondateurs et membres actifs de la fédération. La convocation
                        est adressée par le(la) Secrétaire général(e) conformément à l’article 9 ;</li>
                    <li>La convocation doit être envoyée au moins 21 jours calendaires avant la date de
                        l’assemblée générale, et doit préciser l’intégralité des points inscrits à l’ordre du jour
                        et des résolutions proposées au vote de l’assemblée générale ;</li>
                    <li>Tout membre peut se faire représenter en donnant une procuration de vote, par mandat
                        écrit ou par e-mail, à la personne adhérente de la fédération de son choix, à condition
                        qu’il en soit adressé une copie au Secrétaire général et que la personne ou le groupe
                        ou l’association… soit adhérent(e) à la fédération. Chaque participant à l’assemblée
                        générale ne peut recevoir plus d’un mandat à l’exception des membres du bureau, qui
                        peuvent recevoir plusieurs mandats ;</li>
                    <li>Tout membre fondateur ou membre actif peut, dans un délai de 2 semaines avant la
                        date de l’assemblée générale, demander l’inscription à l’ordre du jour d’un point et/ou
                        d’une résolution supplémentaire, que le Président et le Secrétaire général doivent
                        ajouter à l’ordre du jour. En cas d’urgence, ce délai peut être réduit à 3 jours ;
                        l’inscription du point urgent à examiner se fait dans les mêmes conditions que les
                        autres points évoqués à l’ordre du jour initial.</li>
                </ul>
                </p>
                <p>
                    Les votes ont lieu à main levée. Toutefois, en cas de demande du bureau ou de 20 % des
                    membres actifs présents, un scrutin à bulletin secret peut avoir lieu.
                </p>
                <p>
                    L’assemblée générale se réunit en séance extraordinaire à chaque fois que les intérêts de la
                    Fédération l’exigent, soit sur demande du bureau, soit sur demande des 2/3 des membres
                    actifs.
                </p>
                <h5>Article 11 : Délégué de région, département</h5>
                <p>
                    Sur proposition de tout adhérent de la Fédération, le bureau nomme des délégués de région,
                    de département parmi les adhérents. Ces délégués “organisent” le territoire dont ils ont la
                    charge, comme ils le souhaitent, mais en accord avec l’objet de l’association et le règlement
                    intérieur.
                </p>
                <p>
                    Les délégués de région écoutent, encouragent la prise de parole, organisent les échanges et
                    les manifestations, initient des projets, détectent les talents et les futurs leaders. Ils valident
                    les initiatives d’animation locale.
                </p>
                <p>
                    Ils organisent ainsi le maillage territorial de la fédération suivant les modalités prévues par le
                    règlement intérieur.
                </p>
                <p>
                    Les conflits entre adhérents ou entre le bureau avec un adhérent seront du ressort du Comité
                    d’Éthique et de Déontologie et des membres fondateurs. Le bureau et/ou l’assemblée générale
                    sont compétents pour les destituer et dissoudre leurs équipes. En cas de manquement grave,
                    le Président ou le Vice-Président peuvent les suspendre à tout moment (cf. article 7).
                </p>
                <h5>Article 12 : Règlement intérieur - Dissolution</h5>
                <p>
                    Le Comité d’Ethique et de Déontologie rédige le règlement intérieur afin de compléter les
                    dispositions non-prévues par les statuts. Ce règlement est adopté par vote de l’assemblée
                    générale. L’adhésion aux statuts emporte de plein droit l’adhésion au règlement intérieur. La
                    Fédération peut être dissoute sur proposition du Président, Vice-Président ou d’une majorité
                    simple de l’assemblée générale.
                </p>
                <h5>Article 13 : Clause particulière</h5>
                <p>
                    Nous allons élargir le bureau et recruter les équipes plus tard. En l’absence du trésorier, le
                    (la) président(e) va s’occuper de la trésorerie. Notre fédération va rémunérer les dirigeants
                    sans mettre en cause son caractère de non lucratif
                </p>
